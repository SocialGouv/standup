import { formatSeconds } from "../formatSeconds";

test.each`
  seconds | time
  ${0}    | ${'00:00'}
  ${1}    | ${'00:01'}
  ${5}    | ${'00:05'}
  ${10}   | ${'00:10'}
  ${15}   | ${'00:15'}
  ${20}   | ${'00:20'}
  ${25}   | ${'00:25'}
  ${30}   | ${'00:30'}
  ${35}   | ${'00:35'}
  ${40}   | ${'00:40'}
  ${45}   | ${'00:45'}
  ${50}   | ${'00:50'}
  ${55}   | ${'00:55'}
  ${59}   | ${'00:59'}
  ${60}   | ${'01:00'}
  ${61}   | ${'01:01'}
  ${65}   | ${'01:05'}
  ${70}   | ${'01:10'}
  ${75}   | ${'01:15'}
  ${80}   | ${'01:20'}
  ${85}   | ${'01:25'}
  ${90}   | ${'01:30'}
  ${95}   | ${'01:35'}
  ${100}  | ${'01:40'}
  ${105}  | ${'01:45'}
  ${110}  | ${'01:50'}
  ${115}  | ${'01:55'}
  ${119}  | ${'01:59'}
  ${120}  | ${'02:00'}
  ${121}  | ${'02:01'}
  ${179}  | ${'02:59'}
  ${180}  | ${'03:00'}
  ${181}  | ${'03:01'}
`('returns $time for $seconds secondes', ({seconds, time}) => {
  expect(formatSeconds(seconds)).toEqual(time);
})
